package com.yourgame.utils;

public final class AngleSlope {
    private final float angle;
    private final float slope;

    public AngleSlope(float angle, float slope) {
        this.angle = angle;
        this.slope = slope;
    }

    public float getAngle() {
        return angle;
    }

    public float getSlope() {
        return slope;
    }

}
